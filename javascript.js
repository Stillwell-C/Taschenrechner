let operator
let total = 0


function operate(num1, num2){
    if (operator = add){
        return total = num1 + num2;
    } else if (operator = subtract) {
        return total = num1 - num2;
    } else if (operator = multiply) {
        return total = num1 * num2;
    } else if (operator = divide) {
        return total = num1 / num2;
    }
}